#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#include <limits.h>
#define NODE_MAX 1001
#define EDGE_MAX 200001 // 양방향 간선이므로 200,000개

typedef struct {
	int cost;
	int node;
} Edge;

void swap(Edge* a, Edge* b) {
	Edge tmp;
	tmp.cost = a->cost;
	tmp.node = a->node;
	a->cost = b->cost;
	a->node = b->node;
	b->cost = tmp.cost;
	b->node = tmp.node;
}

typedef struct {
	Edge* heap[EDGE_MAX];
	int count;
} PriorityQueue;

void push(PriorityQueue* pq, Edge* edge) {
	if (pq->count >= EDGE_MAX) return;

	pq->heap[pq->count] = edge;
	int now = pq->count;
	int parent = (now - 1) / 2;

	// 새 원소를 삽입후 상향식으로 힙 구성
	// cost가 낮은 edge가 위로 올수 있도록
	while (now > 0 && pq->heap[now]->cost < pq->heap[parent]->cost) {
		swap(pq->heap[now], pq->heap[parent]);
		now = parent;
		parent = (now - 1) / 2;
	}

	pq->count++;
}

Edge* pop(PriorityQueue* pq) {
	if (pq->count <= 0) return NULL;

	Edge* res = pq->heap[0];
	pq->count--;
	pq->heap[0] = pq->heap[pq->count];
	
	int now = 0;
	int leftChild = 1;
	int rightChild = 2;
	int target = now;

	// 하향식으로 힙 구성
	while (leftChild < pq->count) {
		if (pq->heap[target]->cost > pq->heap[leftChild]->cost) target = leftChild;

		if (rightChild < pq->count && pq->heap[target]->cost > pq->heap[rightChild]->cost) target = rightChild;

		if (target == now) break;

		swap(pq->heap[now], pq->heap[target]);
		now = target;
		leftChild = now * 2 + 1;
		rightChild = now * 2 + 2;
	}

	return res;
}

typedef struct Node {
	Edge* data;
	struct Node* next;
} Node;

Node** adj;
int d[NODE_MAX];

void addNode(Node** target, int index, Edge* edge) {
	if (target[index] == NULL) {
		target[index] = (Node*)malloc(sizeof(Node));
		target[index]->data = edge;
		target[index]->next = NULL;
	}
	else {
		Node* node = (Node*)malloc(sizeof(Node));
		node->data = edge;
		node->next = target[index];
		target[index] = node;
	}
}

int main(void) {
	// 정점과 간선의 개수
	int n, m;
	scanf("%d %d", &n, &m);

	// 인접리스트 선언 및 초기화
	adj = (Node**)malloc(sizeof(Node*) * (n + 1));
	for (int i = 1; i <= n; i++) adj[i] = NULL;

	// 우선순위큐 선언 및 초기화
	PriorityQueue* pq = (PriorityQueue*)malloc(sizeof(PriorityQueue));
	pq->count = 0;

	// 연결리스트 초기화
	for (int i = 0; i < m; i++) {
		// a 정점에서 b 정점 가는 비용은 c
		// b 정점에서 a 정점 가는 비용은 c
		int a, b, c;
		scanf("%d %d %d", &a, &b, &c);

		Edge* edge1 = (Edge*)malloc(sizeof(Edge));
		edge1->node = b;
		edge1->cost = c;
		addNode(adj, a, edge1);

		Edge* edge2 = (Edge*)malloc(sizeof(Edge));
		edge2->node = a;
		edge2->cost = c;
		addNode(adj, b, edge2);
	}

	// 프림 알고리즘 시작
	long long res = 0;
	Edge* start = (Edge*)malloc(sizeof(Edge));
	start->cost = 0;
	start->node = 1;
	push(pq, start);
	
	for (int i = 1; i <= n; i++) {
		int nextNode = -1;
		int nextCost = INT_MAX;

		while (1) {
			Edge* now = pop(pq);

			if (now == NULL) break;

			nextNode = now->node;

			// 해당 정점이 연결되지 않았을 때 break
			if (!d[nextNode]) {
				nextCost = now->cost;
				break;
			}
		}

		// 다음 정점을 찾지 못했다면 연결 그래프가 아님.
		if (nextCost == INT_MAX) printf("연결 그래프가 아님. \n");

		// 해당 간선 비용 누적
		res += nextCost;
		// 해당 정점 연결 표시
		d[nextNode] = 1;

		// 다음 정점과 연결된 간선을 모두 우선순위큐에 삽입
		Node* cur = adj[nextNode];
		while (cur != NULL) {
			push(pq, cur->data);
			cur = cur->next;
		}
	}

	printf("%lld\n", res);
	system("pause");
	return 0;
}